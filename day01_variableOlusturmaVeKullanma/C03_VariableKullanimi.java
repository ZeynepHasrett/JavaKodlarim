package day01_variableOlusturmaVeKullanma;

public class C03_VariableKullanimi {

    public static void main(String[] args) {

        // Kullanicinin hesap bakiyesini tutan bir variable olusturma

        double bakiye = 0;

        // Kullanicinin hesap bakiyesini 100 artirin

        bakiye = bakiye + 100;

        System.out.println("15. satirda bakiye : " + bakiye );

        // bakiye miktarini 3 katina cikarin

        bakiye = bakiye * 3;

        System.out.println("21. satirda bakiye : " + bakiye );



    }


}
