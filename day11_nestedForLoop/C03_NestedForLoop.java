package day11_nestedForLoop;

public class C03_NestedForLoop {

    public static void main(String[] args) {

        /*
             Asagidaki sekli olusturacak bir kod yaziniz

             * * * * * * *
             * * * * * * *
             * * * * * * *
             * * * * * * *

         */


        for (int i = 1; i <= 4; i++) {

            for (int j = 1; j <= 7; j++) {
                System.out.print("* ");
            }

            System.out.println("");

        }

    }

}
